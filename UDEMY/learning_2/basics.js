// functional programming javascript
// functional programming means all functions about input x-----> Function f: --------> output f(x)

var name = 'Barbie doll';
var greeting = "Hey how are you?";

console.log(greeting + ', ' + 'Hey how are you?');


// imperative vs Declarative

function greetingOne(name, greeting) {
    return(greeting + ', ' + 'Hey how are you?');
}
// This is declarative
// This is a Pure Functions
greetingOne(name, greeting);

